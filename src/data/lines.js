// 找兩站之間的路線座標段
function closestIdx(coords, lat, lng) {
  let minD = Infinity, idx = 0
  coords.forEach(([la, lo], i) => {
    const d = (la - lat) ** 2 + (lo - lng) ** 2
    if (d < minD) { minD = d; idx = i }
  })
  return { idx, dist: Math.sqrt(minD) }
}

export function getRoutePath(origin, dest) {
  const THRESHOLD = 0.18
  for (const coords of Object.values(LINE_PATHS)) {
    const o = closestIdx(coords, origin.lat, origin.lng)
    const d = closestIdx(coords, dest.lat, dest.lng)
    if (o.dist < THRESHOLD && d.dist < THRESHOLD && o.idx !== d.idx) {
      const [s, e] = o.idx < d.idx ? [o.idx, d.idx] : [d.idx, o.idx]
      const seg = coords.slice(s, e + 1)
      if (seg.length > 1) return seg
    }
  }
  // 跨線路段：合併兩段
  for (const coords of Object.values(LINE_PATHS)) {
    const o = closestIdx(coords, origin.lat, origin.lng)
    if (o.dist < THRESHOLD) {
      for (const coords2 of Object.values(LINE_PATHS)) {
        if (coords === coords2) continue
        const d = closestIdx(coords2, dest.lat, dest.lng)
        if (d.dist < THRESHOLD) {
          return [
            ...coords.slice(0, o.idx + 1).reverse(),
            ...coords2.slice(0, d.idx + 1),
          ]
        }
      }
    }
  }
  return [[origin.lat, origin.lng], [dest.lat, dest.lng]]
}
